package albumcollection.album;

/**
 * A simple interface to simulate playing audio
 *
 * @author Zachary Goldberg
 */
public interface IPlayable {

   /**
    * A stub for the <b><code>play()</code></b> method
    */
   public void play();

   /**
    * A stub for the <b><code>fastForward()</code></b> method
    */
   public void fastForward();

   /**
    * A stub for the <b><code>rewind()</code></b> method
    */
   public void rewind();

   /**
    * A stub for the <b><code>skipToNext()</code></b> method
    */
   public void skipToNext();

   /**
    * A stub for the <b><code>previousTrack()</code></b> method
    */
   public void previousTrack();
}
